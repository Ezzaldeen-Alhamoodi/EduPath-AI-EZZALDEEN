/* EduPath AI v5.4.6 Isolated Native Arabic Tasks Engine
   This file is the only frontend engine for the Tasks form.
   It intentionally ignores old task translation layers in app.js. */
(function () {
    "use strict";

    const OLD_TO_AR = {
        "أخرى": "أخرى",
        "مرة واحدة / بدون تكرار": "مرة واحدة / بدون تكرار",
        "يوميًا": "يوميًا",
        "أسبوعيًا": "أسبوعيًا",
        "شهريًا": "شهريًا",
        "أيام محددة": "أيام محددة"
    };

    function normalize(value) {
        if (value === undefined || value === null) return "";
        const text = String(value).trim();
        return OLD_TO_AR[text] || text;
    }

    function qs(id) { return document.getElementById(id); }

    function isOther(value) {
        return normalize(value) === "أخرى";
    }

    function unique(values) {
        return [...new Set((values && values.length ? values : ["أخرى"]).map(normalize))];
    }

    function setLabel(id, text) {
        const el = qs(id);
        if (el) el.textContent = text;
    }

    function setPlaceholder(id, text) {
        const el = qs(id);
        if (el) el.placeholder = text;
    }

    function fillSelect(select, values, oldValue) {
        if (!select) return;
        const normalizedValues = unique(values);
        const selected = normalize(oldValue || select.dataset.current || select.value || "");
        select.innerHTML = "";
        normalizedValues.forEach(value => {
            const option = document.createElement("option");
            option.value = value;
            option.textContent = value;
            if (selected === value) option.selected = true;
            select.appendChild(option);
        });
        if (!select.value && normalizedValues.length) select.value = normalizedValues[0];
    }

    function getData() {
        return window.EDUPATH_TASKS_AR_DATA || {};
    }

    function getConfig(type) {
        const data = getData();
        const normalizedType = normalize(type) || "عام";
        return data[normalizedType] || data["عام"];
    }

    function getDetailValues(config, topic, skill) {
        const t = normalize(topic);
        const s = normalize(skill);
        if (!config || !config.detail) return ["موضوع عام", "أخرى"];
        return config.detail[s] || config.detail[t] || config.detail["أخرى"] || ["موضوع عام", "أخرى"];
    }

    function getTrainingValues(config, topic, skill, detail) {
        if (!config) return ["تنفيذ مهمة", "أخرى"];
        // The current phase keeps training stable and Arabic-source.
        return config.training || ["تنفيذ مهمة", "أخرى"];
    }

    function toggleBoxes() {
        const category = qs("categorySelect");
        const topic = qs("topicSelect");
        const skill = qs("skillSelect");
        const detail = qs("detailedTopicSelect");
        const training = qs("trainingTypeSelect");
        const pairs = [
            ["customCategoryBox", category && category.value],
            ["customTopicBox", topic && topic.value],
            ["customSkillBox", skill && skill.value],
            ["customDetailedTopicBox", detail && detail.value],
            ["customTrainingTypeBox", training && training.value]
        ];
        pairs.forEach(([id, value]) => {
            const box = qs(id);
            if (box) box.style.display = isOther(value) ? "block" : "none";
        });
    }

    function applyNativeLabels() {
        setLabel("taskNameLabel", "اسم المهمة");
        setLabel("topicLabel", "الفئة الرئيسية");
        setLabel("skillLabel", "الفئة الفرعية");
        setLabel("detailLabel", "الموضوع التفصيلي");
        setLabel("trainingLabel", "نوع النشاط");
        setLabel("sourceLabel", "المصدر أو الرابط");
        setLabel("difficultyLabel", "مستوى الصعوبة من 1 إلى 5");
        setLabel("priorityLabel", "الأولوية من 1 إلى 5");
        setLabel("expectedTimeLabel", "الوقت المتوقع بالدقائق");
        setLabel("startDateLabel", "تاريخ البدء");
        setLabel("endDateLabel", "تاريخ الانتهاء أو الموعد النهائي");
        setLabel("reminderLabel", "وقت التذكير");
        setLabel("repeatLabel", "التكرار");
        setLabel("repeatDaysLabel", "أيام التكرار");
        setLabel("notesLabel", "ملاحظات إضافية");
        setLabel("repeatLabel", "اكتب نمط التكرار الذي تريده");
        setPlaceholder("taskTitleInput", "مثال: مراجعة حفظ سورة النساء");
        setPlaceholder("sourceInput", "كتاب، موقع إلكتروني، فيديو تعليمي، ملف، أو أي مصدر آخر");
        setPlaceholder("notesInput", "اكتب أي ملاحظات أو خطة أو تعليمات شخصية");
        setPlaceholder("repeatCustomInput", "اكتب ما تريد");
        ["customCategoryInput", "customTopicInput", "customSkillInput", "customDetailedTopicInput", "customTrainingTypeInput"].forEach(id => setPlaceholder(id, "اكتب ما تريد"));
    }

    const REPEAT_LABELS_AR = {
        "مرة واحدة / بدون تكرار": "مرة واحدة / بدون تكرار",
        "يوميًا": "يوميًا",
        "أسبوعيًا": "أسبوعيًا",
        "شهريًا": "شهريًا",
        "أيام محددة": "أيام محددة",
        "أخرى": "أخرى"
    };

    function repeatToArabic(value) {
        const text = normalize(value);
        return REPEAT_LABELS_AR[text] || text || "يوميًا";
    }

    function rebuildRepeatSelect() {
        const select = qs("repeatTypeSelect");
        if (!select) return;
        const current = repeatToArabic(select.dataset.current || select.value || "يوميًا");
        const options = ["مرة واحدة / بدون تكرار", "يوميًا", "أسبوعيًا", "شهريًا", "أيام محددة", "أخرى"];
        select.innerHTML = "";
        options.forEach(value => {
            const option = document.createElement("option");
            option.value = value;
            option.textContent = value;
            if (current === value) option.selected = true;
            select.appendChild(option);
        });
        select.value = options.includes(current) ? current : "يوميًا";
        select.dataset.current = select.value;
    }

    function showBox(el, shouldShow) {
        if (!el) return;
        el.hidden = !shouldShow;
        el.style.setProperty("display", shouldShow ? "block" : "none", "important");
        el.setAttribute("aria-hidden", shouldShow ? "false" : "true");
    }

    function updateRepeatVisibility() {
        const select = qs("repeatTypeSelect");
        const daysBox = qs("repeatDaysBox");
        const customBox = qs("repeatCustomBox");
        const value = repeatToArabic(select && select.value);
        showBox(daysBox, value === "أيام محددة");
        showBox(customBox, value === "أخرى");
    }

    function renderTaskTypes() {
        const grid = qs("taskTypeCards");
        const categoryInput = qs("categorySelect");
        if (!grid || !categoryInput) return;
        const data = getData();
        const current = normalize(categoryInput.value || "عام");
        categoryInput.value = current;
        grid.innerHTML = "";
        Object.keys(data).forEach(type => {
            const config = data[type] || {};
            const button = document.createElement("button");
            button.type = "button";
            button.className = "task-type-card" + (type === current ? " active" : "");
            button.dataset.type = type;
            button.innerHTML = `<span>${config.icon || "أ"}</span><strong>${type}</strong>`;
            button.addEventListener("click", () => {
                categoryInput.value = type;
                ["topicSelect", "skillSelect", "detailedTopicSelect", "trainingTypeSelect"].forEach(id => {
                    const el = qs(id);
                    if (el) el.dataset.current = "";
                });
                renderTaskTypes();
                updateFields();
            });
            grid.appendChild(button);
        });
    }

    function updateFields() {
        const categoryInput = qs("categorySelect");
        const topic = qs("topicSelect");
        const skill = qs("skillSelect");
        const detail = qs("detailedTopicSelect");
        const training = qs("trainingTypeSelect");
        if (!categoryInput || !topic || !skill || !detail || !training) return;
        const type = normalize(categoryInput.value || "عام");
        categoryInput.value = type;
        const config = getConfig(type);

        const oldTopic = normalize(topic.dataset.current || topic.value || "");
        fillSelect(topic, config.main || ["أخرى"], oldTopic);

        function refreshSub() {
            const selectedTopic = normalize(topic.value);
            const subValues = (config.sub && (config.sub[selectedTopic] || config.sub["أخرى"])) || ["أخرى"];
            fillSelect(skill, subValues, skill.dataset.current || skill.value);
            refreshDetail();
        }

        function refreshDetail() {
            const detailValues = getDetailValues(config, topic.value, skill.value);
            fillSelect(detail, detailValues, detail.dataset.current || detail.value);
            refreshTraining();
        }

        function refreshTraining() {
            const trainingValues = getTrainingValues(config, topic.value, skill.value, detail.value);
            fillSelect(training, trainingValues, training.dataset.current || training.value);
            toggleBoxes();
        }

        topic.onchange = () => { topic.dataset.current = topic.value; skill.dataset.current = ""; detail.dataset.current = ""; training.dataset.current = ""; refreshSub(); };
        skill.onchange = () => { skill.dataset.current = skill.value; detail.dataset.current = ""; training.dataset.current = ""; refreshDetail(); };
        detail.onchange = () => { detail.dataset.current = detail.value; training.dataset.current = ""; refreshTraining(); };
        training.onchange = () => { training.dataset.current = training.value; toggleBoxes(); };

        refreshSub();
    }

    function initNativeTasks() {
        if (!qs("taskTypeCards")) return;
        applyNativeLabels();
        rebuildRepeatSelect();
        renderTaskTypes();
        updateFields();
        updateRepeatVisibility();
        const repeat = qs("repeatTypeSelect");
        if (repeat) {
            repeat.addEventListener("change", () => {
                repeat.dataset.current = repeat.value;
                updateRepeatVisibility();
            });
            repeat.onchange = () => {
                repeat.dataset.current = repeat.value;
                updateRepeatVisibility();
            };
        }
        [80, 250, 700].forEach(delay => {
            setTimeout(() => {
                applyNativeLabels();
                rebuildRepeatSelect();
                renderTaskTypes();
                updateFields();
                updateRepeatVisibility();
            }, delay);
        });
    }

    document.addEventListener("DOMContentLoaded", initNativeTasks);
    window.EDUPATH_NATIVE_TASKS_INIT = initNativeTasks;
})();
